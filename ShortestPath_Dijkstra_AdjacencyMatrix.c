#define V 16 // V는 그래프의 vertex 수를 정의합니다.


int minDistance(int dist[], bool sptSet[])
{
    int min = INT_MAX, min_index; // min은 최소 거리를, min_index는 최소 거리를 가지는 정점의 인덱스를 저장합니다.

    for (int v = 0; v < V; v++)
        if (sptSet[v] == false && dist[v] <= min)
            min = dist[v], min_index = v;

    return min_index;
}

void printSolution(int dist[])
{
    for (int i = 0; i < V; i++)
        printf("정점 1부터 정점 %d까지의 최단경로 : %d\n", i+1, dist[i]); // 각 정점까지의 최단 거리를 출력합니다.
}

void dijkstra(int graph[V][V], int src)
{
    int dist[V]; 

    bool sptSet[V]; 

    for (int i = 0; i < V; i++)
        dist[i] = INT_MAX, sptSet[i] = false; // 시작하기 전에 모든 정점의 거리를 무한대로 설정하고, sptSet을 false로 설정합니다.

    dist[src] = 0; // 소스 정점의 거리는 0입니다.

    for (int count = 0; count < V-1; count++)
    {
        int u = minDistance(dist, sptSet); 

        sptSet[u] = true;

        for (int v = 0; v < V; v++)

            if (!sptSet[v] && graph[u][v] && dist[u] != INT_MAX
                && dist[u]+graph[u][v] < dist[v])
                dist[v] = dist[u] + graph[u][v];
    }

    printSolution(dist);
}

